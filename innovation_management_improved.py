def run_sensitivity(wh_preds, pr_preds, noise=0.0, wear=12.5, price_mult=1.0):
    moves, profits = 0, []
    angle = 15.0
    for w, p in zip(wh_preds[:N_SIM], pr_preds[:N_SIM]):
        w_n = w + np.random.normal(0, noise)
        p_n = p * price_mult
        dec, dp, uh, um = tracker_decision_economic(w_n, p_n, angle, C_m=wear)
        if dec == 'MOVE':
            angle = (angle + DELTA_THETA) % 90
            moves += 1
            profits.append(um - MARGINAL_COST_RS_WH * wear)
        else:
            profits.append(uh)
    return moves, np.sum(profits)

