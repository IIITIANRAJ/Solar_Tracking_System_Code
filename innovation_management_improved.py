"""---
## SECTION 8: Causal Framework — DiD / DML (Improvement #1)
"""

# ─────────────────────────────────────────────
# CELL 13: Difference-in-Differences (DiD)
# IMPROVEMENT #1: Causal pathway: tracking → energy yield → price stability
# Treatment: days with active AI tracking (high GHI, MOVE decisions)
# Outcome: price volatility (rolling 7-day std)
# ─────────────────────────────────────────────

# Build daily panel for DiD
test_df = df.iloc[TIME_STEP + SPLIT:TIME_STEP + SPLIT + len(y_s_pred)].copy().reset_index(drop=True)
test_df['solar_pred'] = y_s_pred
test_df['price_pred'] = y_m_pred

# Define treatment: days with above-median predicted solar output (AI-tracked high yield days)
median_solar = np.median(y_s_pred)
test_df['treated'] = (test_df['solar_pred'] > median_solar).astype(int)

# Outcome: price volatility (std of price in 7-day rolling window)
test_df['price_vol'] = test_df['Modal Price (Rs./Quintal)'].rolling(96*7, min_periods=1).std()

# Pre/Post period: first half vs second half of test set
mid = len(test_df) // 2
test_df['post'] = (test_df.index >= mid).astype(int)

# DiD estimate
# ATT = (treated_post - treated_pre) - (control_post - control_pre)
groups = test_df.groupby(['treated', 'post'])['price_vol'].mean()
did_estimate = (groups[1,1] - groups[1,0]) - (groups[0,1] - groups[0,0])

print('Difference-in-Differences (DiD) Analysis')
print('─'*55)
print(f'  Group Mean Price Volatility:')
print(f'  Treated  (Pre) : {groups[1,0]:.2f} Rs./Quintal')
print(f'  Treated  (Post): {groups[1,1]:.2f} Rs./Quintal')
print(f'  Control  (Pre) : {groups[0,0]:.2f} Rs./Quintal')
print(f'  Control  (Post): {groups[0,1]:.2f} Rs./Quintal')
print(f'  DiD ATT Estimate: {did_estimate:.2f} Rs./Quintal')
print(f'  Interpretation: AI-tracked high-yield days are associated with')
print(f'  a {abs(did_estimate):.2f} Rs./Quintal {"reduction" if did_estimate < 0 else "change"} in price volatility.')
print('─'*55)

# ─────────────────────────────────────────────
# CELL 14: Double Machine Learning (DML)
# IMPROVEMENT #1: Estimate causal effect of solar adoption on price stability
# using partialling-out estimator (Robinson 1988)
# ─────────────────────────────────────────────

from sklearn.linear_model import Ridge
from sklearn.model_selection import cross_val_predict

# DML setup
# Y = price_volatility (outcome)
# T = treated (solar adoption indicator)
# X = confounders (weather, time)

dml_df = test_df[['price_vol','treated','GHI','temp','humidity','hour_sin','month_sin','doy_sin']].dropna()

Y = dml_df['price_vol'].values
T = dml_df['treated'].values
X = dml_df[['GHI','temp','humidity','hour_sin','month_sin','doy_sin']].values

# Step 1: Partial out confounders from Y
m_Y = Ridge(alpha=1.0)
Y_resid = Y - cross_val_predict(m_Y, X, Y, cv=5)

# Step 2: Partial out confounders from T
m_T = Ridge(alpha=1.0)
T_resid = T - cross_val_predict(m_T, X, T, cv=5)

# Step 3: DML estimate θ = Cov(Y_resid, T_resid) / Var(T_resid)
theta_dml = np.sum(T_resid * Y_resid) / np.sum(T_resid ** 2)

# Standard error (sandwich)
psi = T_resid * (Y_resid - theta_dml * T_resid)
se_dml = np.sqrt(np.mean(psi**2) / (np.mean(T_resid**2)**2 * len(Y)))

print('Double Machine Learning (DML) Causal Estimate')
print('─'*55)
print(f'  θ_DML (ATE): {theta_dml:.4f} Rs./Quintal per unit treatment')
print(f'  Std Error  : {se_dml:.4f}')
print(f'  95% CI     : [{theta_dml - 1.96*se_dml:.4f}, {theta_dml + 1.96*se_dml:.4f}]')
print(f'  Causal Pathway: Solar Tracking → Energy Yield → Price Stability')
print('─'*55)

