# ─────────────────────────────────────────────
# CELL 9: Prescriptive Decision Engine — Profit Maximization
# IMPROVEMENT #3: Formal economic interpretation
#   MOVE if ΔRevenue − WearCost > 0
#   ΔRevenue = ΔEnergy × (Price/1000) × PV_efficiency
# IMPROVEMENT #5: Marginal cost of energy included
# ─────────────────────────────────────────────

# Economic constants (literature-grounded)
MOTOR_WEAR_COST_WH   = 12.5   # Wh equivalent per actuation (empirical estimate)
PANEL_AREA_M2        = 1.6    # Typical 1.6m² residential PV panel
PV_EFFICIENCY_RATED  = 0.18   # 18% rated efficiency (monocrystalline)
DELTA_THETA          = 5.0    # Degrees — minimum tracker increment
MARGINAL_COST_RS_WH  = 0.008  # Rs./Wh (grid marginal cost reference)

def tracker_decision_economic(predicted_wh_norm, predicted_price, current_angle_deg,
                               pv_eff=PV_EFFICIENCY_RATED, C_m=MOTOR_WEAR_COST_WH):
    """
    Profit-maximizing MOVE/HOLD decision.
    MOVE iff: ΔRevenue − WearCost > 0
    ΔRevenue = [E(θ+Δθ) − E(θ)] × (Price_Rs / 1000) × pv_eff
    E(θ) = predicted_wh × cos(θ)  [cosine irradiance correction]
    Returns: decision, delta_profit, utilities
    """
    price_weight = max(predicted_price, 0) / 1000  # normalize to Rs. per Wh

    E_hold = predicted_wh_norm * np.cos(np.radians(current_angle_deg))
    E_move = predicted_wh_norm * np.cos(np.radians(current_angle_deg + DELTA_THETA))

    # Revenue from each option (Rs.)
    rev_hold = E_hold * price_weight * pv_eff
    rev_move = E_move * price_weight * pv_eff

    # Profit differential: ΔRevenue − WearCost
    delta_profit = (rev_move - rev_hold) - MARGINAL_COST_RS_WH * C_m

    decision = 'MOVE' if delta_profit > 0 else 'HOLD'
    return decision, delta_profit, rev_hold, rev_move

