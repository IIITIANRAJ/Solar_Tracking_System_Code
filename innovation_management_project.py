def test_system_prediction(sample_index=500):
    # Prepare inputs
    test_sample_solar = X_train_full[sample_index].reshape(1, 24, len(solar_feats))
    test_sample_market = m_X_aligned.iloc[[sample_index]]

    # Predict
    predicted_wh = model_solar.predict(test_sample_solar, verbose=0)[0][0]
    predicted_price = model_market.predict(test_sample_market)[0]

    # Logic
    current_angle = 15
    price_weight = predicted_price / 1000
    motor_cost_wh = 12.5

    utility_move = (predicted_wh * np.cos(np.radians(current_angle + 5)) * price_weight) - motor_cost_wh
    utility_stay = (predicted_wh * np.cos(np.radians(current_angle)) * price_weight)

    print(f"Predicted Solar: {predicted_wh:.2f} Wh | Predicted Price: Rs. {predicted_price:.2f}")
    if utility_move > utility_stay:
        print(f"DECISION: MOVE TRACKER TO {current_angle + 5}°")
    else:
        print("DECISION: HOLD POSITION")

test_system_prediction()

